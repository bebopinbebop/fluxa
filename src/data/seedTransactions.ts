type TransactionSeed = Pick<
  Transaction,
  | 'amount'
  | 'authorized_date'
  | 'category'
  | 'category_id'
  | 'date'
  | 'logo_url'
  | 'merchant_entity_id'
  | 'merchant_name'
  | 'name'
  | 'payment_channel'
  | 'pending'
  | 'personal_finance_category'
  | 'personal_finance_category_icon_url'
  | 'transaction_id'
  | 'transaction_type'
  | 'website'
> & {
  accountId: string;
};

function merchantCounterparty(seed: TransactionSeed): Transaction['counterparties'][number] {
  return {
    confidence_level: seed.merchant_name ? 'VERY_HIGH' : null,
    entity_id: seed.merchant_entity_id,
    logo_url: seed.logo_url,
    name: seed.merchant_name ?? seed.name,
    phone_number: null,
    type: seed.merchant_name ? 'merchant' : 'other',
    website: seed.website,
  };
}

function createTransaction(seed: TransactionSeed): Transaction {
  return {
    account_id: seed.accountId,
    account_owner: null,
    amount: seed.amount,
    authorized_date: seed.authorized_date,
    authorized_datetime: null,
    category: seed.category,
    category_id: seed.category_id,
    check_number: null,
    counterparties: [merchantCounterparty(seed)],
    date: seed.date,
    datetime: null,
    iso_currency_code: 'USD',
    location: emptyLocation,
    logo_url: seed.logo_url,
    merchant_entity_id: seed.merchant_entity_id,
    merchant_name: seed.merchant_name,
    name: seed.name,
    payment_channel: seed.payment_channel,
    payment_meta: emptyPaymentMeta,
    pending: seed.pending,
    pending_transaction_id: null,
    personal_finance_category: seed.personal_finance_category,
    personal_finance_category_icon_url: seed.personal_finance_category_icon_url,
    transaction_code: null,
    transaction_id: seed.transaction_id,
    transaction_type: seed.transaction_type,
    unofficial_currency_code: null,
    website: seed.website,
  };
}

